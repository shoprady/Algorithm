n = int(input())
k = 2 * n - 3
print('*'*n + ' '*k + '*'*n)
for i in range(1, n - 1):
    k -= 2
    print(' '*i + '*' + ' '*(n-2) + '*' + ' '*k + '*' + ' '*(n-2) + '*')
print(' '*(n-1) + '*' + ' '*(n-2) + '*' + ' '*(n-2) + '*')
for i in range(n - 2, 0, -1):
    print(' '*i + '*' + ' '*(n-2) + '*' + ' '*k + '*' + ' '*(n-2) + '*')
    k += 2
print('*'*n + ' '*(2*n-3) + '*'*n)